function valorinvertido(){
    var numeroA=parseFloat(prompt("Informe o valor A: "));
    var numeroB=parseInt(prompt("Informe o valor B: "));
    var numeroC=numeroA;
    var numeroA=numeroB;
    var numeroB=numeroC;
    alert("O número correspondente de A é: " + numeroA + " e o correspondente de B é: " + numeroC);
}